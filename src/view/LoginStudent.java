    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        if(checkDL()){
            String email = txtemaillogin.getText();
            String pass = txtpass.getText();
            if(email.equals("admin@gmail.com")&&pass.equals("admin")){
                TeacherManager quanly = new TeacherManager();
                quanly.setVisible(true);
                quanly.setExtendedState(JFrame.MAXIMIZED_BOTH);
                this.dispose();
            }
            else{
                boolean check = false;
                Student stu = new Student();
                List<Student> list = studentService.getAllStudent();
                for(Student a:list){
                    if(a.getEmail().equals(email)){
                        if(a.getPassword()==null){
                            check = false;
                            break;
                        }
                        if(a.getPassword().equals(pass)){
                            stu = a;
                            check = true;
                            break;
                        }
                        
                    }
                }
                if(check){
                    StudentManager studentManager = new StudentManager(stu);
                    studentManager.setVisible(true);
                    studentManager.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Mật khẩu sai", "Lỗi", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

