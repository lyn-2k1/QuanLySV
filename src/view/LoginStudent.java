    private boolean checkDL(){
        if(!Pattern.matches("^[a-zA-z0-9_-]+(@gmail.com)$", txtemaillogin.getText())){
            JOptionPane.showMessageDialog(this, "Email có dạng x..x@gmail.com", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;           
        }else if(txtpass.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập mật khẩu", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false; 
        }else return true;
    }
