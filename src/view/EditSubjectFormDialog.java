    public EditSubjectFormDialog(java.awt.Frame parent, boolean modal,Subject sub) {
        super(parent, modal);
        initComponents();
        setLocationRelativeTo(this);
        setResizable(false);
        subjectService = new SubjectService();
        for(int i = 0 ; i < cbMaNganh.getItemCount(); i++){
            if(cbMaNganh.getItemAt(i).subSequence(0, 2).equals(sub.getMa().substring(0, 2))){
                if(cbMaNganh.getItemAt(i).equals("DT")){
                    cbMaNganh.setSelectedIndex(2);
                    break;
                }
                else if(cbMaNganh.getItemAt(i).equals("DTVT")){
                    cbMaNganh.setSelectedIndex(3);
                    break;
                }
                else{
                    cbMaNganh.setSelectedIndex(i);
                    break;
                }
            }
        }
        txtten.setText(sub.getTen());
        txtTinchi.setText(String.valueOf(sub.getTinchi()));
        txtThanhTien.setText(String.valueOf(sub.getGiatien()));
        
    }

