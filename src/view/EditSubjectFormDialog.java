    private boolean checkDL() {
        if(txtten.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Tên môn học rỗng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false; 
        } else if(!Pattern.matches("\\d{1}$", txtTinchi.getText())){
            JOptionPane.showMessageDialog(this, "Số tín chỉ nhỏ hơn 10", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }else return true;
    }
