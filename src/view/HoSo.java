    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(checkDL()){
            String id = txtID.getText();
            String name = txtName.getText();
            String ns = txtNgaySinh.getText();
            String cmt = txtCMT.getText();
            String gioiTinh = cbGioiTinh.getSelectedItem().toString();
            Float diemXT = Float.valueOf(txtDiemXetTuyen.getText());
            String email = txtEmail.getText();
            String diaChi = txtDiaChi.getText();
            String sdt = txtSoDienThoai.getText();
            String nganhHoc = cbNganhHoc.getSelectedItem().toString();
            boolean hocBong = false;
            if(diemXT >= 25){
                hocBong = true;
            }
            Student std = new Student(id,name,cmt,ns,gioiTinh,diemXT,email,diaChi,sdt,nganhHoc,hocBong);
            std.setPassword(student.getPassword());
            studentService.editStudent(std);
            this.dispose();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

