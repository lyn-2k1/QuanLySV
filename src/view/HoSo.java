    private boolean checkDL() {
        if(!Pattern.matches("^([a-z A-ZÀÁÂÃÈÉÊÌÍÒÓÔÕÙÚĂĐĨŨƠàáâãèéêìíòóôõùúăđĩũơƯĂẠẢẤẦẨẪẬẮẰẲẴẶẸẺẼỀỀỂưăạảấầẩẫậắằẳẵặẹẻẽềềểỄỆỈỊỌỎỐỒỔỖỘỚỜỞỠỢỤỦỨỪễệỉịọỏốồổỗộớờởỡợụủứừỬỮỰỲỴÝỶỸửữựỳỵỷỹ])[a-z A-ZÀÁÂÃÈÉÊÌÍÒÓÔÕÙÚĂĐĨŨƠàáâãèéêìíòóôõùúăđĩũơƯĂẠẢẤẦẨẪẬẮẰẲẴẶẸẺẼỀỀỂưăạảấầẩẫậắằẳẵặẹẻẽềềểỄỆỈỊỌỎỐỒỔỖỘỚỜỞỠỢỤỦỨỪễệỉịọỏốồổỗộớờởỡợụủứừỬỮỰỲỴÝỶỸửữựỳỵỷỹ]+$", txtName.getText())){
            JOptionPane.showMessageDialog(this, "Họ và tên chỉ có chữ cái", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }else if(!Pattern.matches("^\\d{4}-((0[1-9]{1})|(1[0-2]{1}))-((0[1-9]{1})|([1-2]{1}[0-9]{1})|(3(0|1)))$", txtNgaySinh.getText())){
            JOptionPane.showMessageDialog(this, "Ngày sinh năm-tháng-ngày (yyyy-mm-dd)", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }else if(!Pattern.matches("\\d{12}$", txtCMT.getText())){
            JOptionPane.showMessageDialog(this, "Chứng minh thư chỉ có 12 chữ số", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false; 
        }else if(!Pattern.matches("(^-)*\\d+(.\\d+)*", txtDiemXetTuyen.getText())){
            JOptionPane.showMessageDialog(this, "Điểm xét tuyển sai định dạnh", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;                  
        }else if(!Pattern.matches("^[a-zA-z0-9_-]+(@gmail.com)$", txtEmail.getText())){
            JOptionPane.showMessageDialog(this, "Email có dạng x..x@gmail.com", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }else if(txtDiaChi.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Địa chỉ chưa tồn tại", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;            
        }else if(!Pattern.matches("^(0)\\d{9}$", txtSoDienThoai.getText())){
            JOptionPane.showMessageDialog(this, "SĐT bắt đầu bằng số 0 và có 10 chữ số", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false; 
        }else return true;
    }
